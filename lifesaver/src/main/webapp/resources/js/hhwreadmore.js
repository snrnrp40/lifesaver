function myFunction() {
  var dots = document.getElementById("dots");
  var moreText = document.getElementById("more");
  var moreDots = document.getElementById("moredots");
  var btnText = document.getElementById("myBtn");
  var moreView = document.getElementById("moreview");
  var moreTexts = document.getElementById("moretext");
  if (dots.style.display == "none") {
    dots.style.display = "inline";
    btnText.innerHTML = "Read more";
    moreText.style.display = "none";
    moreDots.style.display = "none";
    moreView.style.display = "none";
    moreTexts.style.display = "none";
  } else {
    dots.style.display = "none";
    moreText.style.display = "inline";
    moreView.style.display = "inline";
    moreDots.style.display = "inline";
    moreTexts.style.display = "none";
    btnText.innerHTML = "Read less";
  }
}

